package com.owl.cocoa.ship;

public class ShipPosition {

    public final double x;
    public final double y;
    public final double z;

    public ShipPosition() {
        x = 0;
        y = 0;
        z = 0;
    }

    public ShipPosition(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
